    {
        $user = Auth::user();

                $mlev = new Masterslevel;
                $mlev->uniname = $request->input('uniname');
                $mlev->myear = $request->input('myear');
                $mlev->program = $request->input('program');
                $mlev->user_id = $user->id;

                $mlev->save();

                $ml = $user->masterslevels;

                return view('mlevel.show',compact('ml'));
    }

